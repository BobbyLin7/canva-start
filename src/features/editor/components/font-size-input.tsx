import { MinusIcon, PlusIcon } from "lucide-react";
import { Button } from "@/components/ui/button";
import { Input } from "@/components/ui/input";

interface FontSizeInputProps {
	value: number;
	onChange: (value: number) => void;
}

export const FontSizeInput = ({ value, onChange }: FontSizeInputProps) => {
	const increment = () => onChange(value + 1);
	const decrement = () => onChange(value - 1);

	const handleChange = (e: React.ChangeEvent<HTMLInputElement>) => {
		const value = Number.parseInt(e.target.value, 10);
		onChange(value);
	};

	return (
		<div className="flex items-center">
			<Button
				onClick={decrement}
				variant="outline"
				className="rounded-r-none border-r-0 p-2"
				size="icon"
			>
				<MinusIcon className="size-4" />
			</Button>
			<Input
				onChange={handleChange}
				value={value}
				className="w-[50px] rounded-none focus-visible:ring-0 focus-visible:ring-offset-0"
			/>
			<Button
				onClick={increment}
				variant="outline"
				className="rounded-l-none border-l-0 p-2"
				size="icon"
			>
				<PlusIcon className="size-4" />
			</Button>
		</div>
	);
};
